package com;
// Integer Arithmetic and Real Arithmetic
class Aa23_Arithmetic
{
    public static void main(String args[])
    {
	// if in expression, all operands are integer then
	// output is also integer
	
	int i = 10;
	int j = 3;
	float ans = i/j;
	System.out.println("ans = " + ans);
	
//=========================================================
	// if in expression any one is real then
	// output is also real

	int x = 10;
	float z = 3;
	System.out.println(x/z);

    }
}