package oops.A7_overriding_n_overridden;

//overriding
/*
if child class is not satisfied with parent method
it can change the method by using concept 
of method overriding
Conditions:
1)only if the class is child class of parent class
then this overriding method is carried out otherwise
it will not support overriding
2)the method of child class should be same
as the method of parent class which we want to 
override
   method name , argument and return type should be also same.
   Co-Varient return types are allowed
   
   
   public > protected > default > private
   
   always keep access modifiers same in both methods
   also child class method can be keep less protected
   from parent class method


*/